# ── Fundamental 지표 Guidebook (각 지표가 무엇을 의미하는지) ──────────────────
_FUNDAMENTAL = """
## 📖 Fundamental Guidebook — 지표 쉽게 이해하기

각 숫자가 **무엇을 뜻하는지**와 **어떻게 보면 되는지**를 쉬운 말로 정리했어요.
(절대적 기준은 없고, **같은 업종끼리 비교**하는 게 핵심입니다.)

### 밸류에이션(비싼지/싼지)
- **Trailing P/E** (후행 주가수익비율): `주가 ÷ 지난 12개월 EPS`. 지금 이익 기준으로
  주가가 이익의 몇 배인지. 낮을수록 싸 보임. (적자면 의미 없음)
- **Forward P/E** (선행 주가수익비율): `주가 ÷ 내년 예상 EPS`. **미래 이익 기준**이라
  성장주는 이걸 더 많이 봄. 예: 20 = "예상 이익의 20배를 주고 사는 것".
- **P/B** (주가순자산비율, PBR): `주가 ÷ 주당 순자산`. 1보다 낮으면 회사 장부가치보다
  싸게 거래. 은행·자산주에서 중요.
- **P/S** (주가매출비율, PSR): `시가총액 ÷ 매출`. 아직 이익이 적은 성장주 볼 때 유용.
  높을수록 매출 대비 비쌈.
- **EV/EBITDA**: `기업가치 ÷ (이자·세금·감가상각 전 이익)`. 부채까지 감안한 '진짜' 밸류.
  낮을수록 저평가. 업종 비교에 매우 유용.
- **PEG**: `Forward P/E ÷ 이익성장률(%)`. 성장까지 감안한 P/E. **1 근처면 적정**,
  1보다 훨씬 낮으면 성장 대비 저평가.

### 성장성 · 수익성
- **Revenue Growth** (매출성장률): 매출이 1년간 얼마나 늘었나. 성장주의 생명선.
- **Earnings Growth** (이익성장률): 순이익이 얼마나 늘었나. 매출보다 이익이 더 빨리
  크면 좋은 신호.
- **Profit Margin** (순이익률): `순이익 ÷ 매출`. 100원 팔아 얼마 남기나. 높을수록 강한 사업.

### 리스크 · 재무 건전성
- **Short Interest (% Float)** (공매도 비율): 유통주식 중 공매도(하락 베팅)된 비율.
  높으면(>10%) 하락 우려가 크다는 뜻이자, 반대로 **숏스퀴즈** 변동성 위험도.
- **Beta** (베타): 시장(지수) 대비 변동성. 1이면 시장과 같이 움직임, 1.5면 1.5배 출렁,
  1보다 작으면 방어적.
- **Net Debt** (순부채): `총부채 − 보유현금`. 마이너스면 현금이 더 많다는 뜻(우량).
- **Debt/Equity** (부채비율): 자본 대비 빚. 높을수록 재무 위험. 업종마다 정상 범위 다름.

### 현금흐름 · 이익 · 위치
- **FCF (TTM)** (잉여현금흐름): `영업현금 − 설비투자`. 회사가 **실제로 자유롭게 쓸 수 있는
  현금**. 이익보다 정직한 지표로, 배당·자사주·부채상환의 원천. 기술주 평가의 핵심.
- **EPS (TTM / Fwd)** (주당순이익): `순이익 ÷ 주식수`. TTM=지난 12개월, Fwd=내년 예상.
  P/E의 분모가 되는 값.
- **52-Week Range** (52주 레인지): 최근 1년 최저~최고가 대비 현재 위치(%).
  0%면 1년 최저, 100%면 최고. 심리·모멘텀 참고용.

> 💡 **한 줄 요약**: P/E·P/S·EV/EBITDA는 *"싼가/비싼가"*, 성장률은 *"빨리 크는가"*,
> FCF·부채는 *"버틸 체력이 있는가"* 를 봅니다. 셋을 같이 봐야 합니다.
"""

